# Basic list creation and indexing

fruits = ["apple", "banana", "cherry", "date", "fig"]

print("Full list:", fruits)
print("First element:", fruits[0])
print("Last element:", fruits[-1])
print("Middle 3 elements:", fruits[1:4])



# output:
# Full list: ['apple', 'banana', 'cherry', 'date', 'fig']
# First element: apple
# Last element: fig
# Middle 3 elements: ['banana', 'cherry', 'date']
