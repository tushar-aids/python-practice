# Iterate over a list using different methods

fruits = ["apple", "banana", "cherry", "date"]

print("Using simple for loop:")
for fruit in fruits:
    print(fruit)

print("\nUsing index:")
for i in range(len(fruits)):
    print(f"fruits[{i}] = {fruits[i]}")



# output:
# Using simple for loop:
# apple
# banana
# cherry
# date

# Using index:
# fruits[0] = apple
# fruits[1] = banana
# fruits[2] = cherry
# fruits[3] = date
